DECLARE @OpsMainTableId INT;
SELECT @OpsMainTableId = TableID FROM SSDL.SPEND_SSDL_Table where TableName = 'OPS_MAIN';

SELECT A.ColumnName as ExistingColumns, A.DisplayColumnName, A.FieldCategory, B.ColumnName as StoryColumns
from SSDL.SPEND_SSDL_TableSchema A
FULL OUTER JOIN @MasterMainTableColumns B ON A.ColumnName = B.ColumnName
WHERE A.TableID = @OpsMainTableId AND A.FieldCategory <> 'ERP - Custom Fields' AND (A.ColumnName IS NULL OR B.ColumnName IS NULL)
ORDER BY A.ColumnName, A.FieldCategory;